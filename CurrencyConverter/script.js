const BASE_URL = "https://v6.exchangerate-api.com/v6/8efc662857fcc2ec548eb0ed/latest";
let dropdowns = document.querySelectorAll(".currselect");
let btn = document.querySelector("button");
let fromCurr = document.querySelector(".from select");
let toCurr = document.querySelector(".to select");
let result = document.querySelector("#result");

for(let sel of dropdowns){
    for(let currCode in countryList){
        let newOption = document.createElement("option");
        newOption.innerText = currCode;
        newOption.value = currCode;
        sel.append(newOption);

        sel.addEventListener("change", (evt) =>{
            updateFlag(evt.target);
        });
    }
}

function updateFlag(element){
    let currCode = element.value;
    let countryCode = countryList[currCode];
    let newSrc = `https://flagsapi.com/${countryCode}/flat/64.png`;
    let img = element.parentElement.querySelector("img");
    img.src = newSrc;
}

btn.addEventListener("click", (evt) =>{
    evt.preventDefault();
    giveExhangeRate();
});

async function giveExhangeRate(){

    let amount = document.querySelector(".amount input");
    let amtVal = amount.value;
    if (amtVal === "" || amtVal < 1) {
      amtVal = 1;
      amount.value = "1";
    }

    const URL = `${BASE_URL}/${fromCurr.value}`;
    
    try{
        let response = await fetch(URL);
        let data = await response.json();
        let rate = data.conversion_rates[toCurr.value];
        let finalAmount = amtVal * rate;
        result.innerText = `${amtVal} ${fromCurr.value} = ${finalAmount.toFixed(2)} ${toCurr.value}`;
    }
    catch (error) {
        result.innerText = "Error fetching exchange rate. Please try again later.";
    }
}